# Q1. What are the three measures of central tendency?

# The three measures of central tendency are:

# Mean: The mean is the sum of all the values in a dataset divided by the number of values in the dataset. It represents the average value of the dataset and is often used as a measure of central tendency when the data is normally distributed.

# Median: The median is the middle value in a dataset when the values are arranged in order from smallest to largest. It represents the value that divides the dataset into two equal parts, and is often used as a measure of central tendency when the data is skewed or contains outliers.

# Mode: The mode is the value in a dataset that appears most frequently. It represents the most common value in the dataset and is often used as a measure of central tendency for categorical or discrete data.